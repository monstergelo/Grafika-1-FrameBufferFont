void draw_kata(int* x, int* y, char* kata){
	int xx = *x;
	int yy = *y;
	int icrx = 32;
	int icry = 40;
	char curr = ' ';
	int i = 0;

	while(i < 1000){
		curr = kata[i];
		if(curr == '\0') break;

		draw_huruf(xx, yy, curr);
		xx += icrx;
		i++;
	}

	*y += icry;
}

